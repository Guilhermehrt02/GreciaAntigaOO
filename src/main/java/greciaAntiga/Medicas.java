package greciaAntiga;

public class Medicas extends Guerra{
	private String imperioPerdedor;
	private Persa persa;
	private LigaDelos delos;
	public String getImperioPerdedor() {
		return imperioPerdedor;
	}
	public void setImperioPerdedor(String imperioPerdedor) {
		this.imperioPerdedor = imperioPerdedor;
	}
	public Persa getPersa() {
		return persa;
	}
	public void setPersa(Persa persa) {
		this.persa = persa;
	}
	public LigaDelos getDelos() {
		return delos;
	}
	public void setDelos(LigaDelos delos) {
		this.delos = delos;
	}
	
}
